function openNav() {
  document.getElementById("mySidenav1").style.width = "100%";
  document.getElementById("main").style.display = "none";
}

function closeNav() {
  document.getElementById("mySidenav1").style.width = "0";
  document.getElementById("main").style.display = "block";
}

